package hello;

		//파일(class)이름 첫글자는 무조건 대문자로!
		//표기법(낙타-카멜): 단어가 합성될떄도 첫글자는 대문자로 함
public class HelloWorld {

	//main() 함수: 메모리(주기억장치)에서 실행
	public static void main(String[] args) {
		// 한줄 주석 - 문자를 출력하는 프로그램 만들기
		System.out.println("Hello~ Java");	//print뒤에 ln을 추가할시 한줄 띄고 출력
		System.out.println("안녕~ 자바");
		
		//자기소개
		System.out.println("이름: 고병진");
		System.out.println("주소: 서울시 마포구");
	}

}
